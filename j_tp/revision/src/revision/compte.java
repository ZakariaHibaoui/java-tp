package revision;

public class compte {
	private int code;
	private double solde;
	
	public compte() {
		super();
		
	}

	public compte(int code, double solde) {
		super();
		this.code = code;
		this.solde = solde;
	}

	public int getCode() {
		return code;
	}

	public double getSolde() {
		return solde;
	}
	
	

}
